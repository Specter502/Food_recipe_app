// Function to get recipes by category
export const getRecipesByCategory = (categoryId: string): Recipe[] => {
  const category = categories.find(c => c.id === categoryId);
  if (!category) return [];
  
  return recipes.filter(recipe => 
    recipe.categories.includes(category.name)
  );
};

// Function to search recipes
export const searchRecipes = (query: string): Recipe[] => {
  if (!query) return [];
  
  const lowercaseQuery = query.toLowerCase();
  
  return recipes.filter(recipe => {
    // Search in title, description, categories and ingredients
    const titleMatch = recipe.title.toLowerCase().includes(lowercaseQuery);
    const descriptionMatch = recipe.description.toLowerCase().includes(lowercaseQuery);
    const categoryMatch = recipe.categories.some(category => 
      category.toLowerCase().includes(lowercaseQuery)
    );
    const ingredientMatch = recipe.ingredients.some(ingredient => 
      ingredient.name.toLowerCase().includes(lowercaseQuery)
    );
    
    return titleMatch || descriptionMatch || categoryMatch || ingredientMatch;
  });
};

